void main() {
  
  /*19. Utilizando if-else, determine e imprima qual o maior número entre dois números x e y.*/
  
  int x = 20;
  int y = 50;
  
  if(x > y){
    print("O maior número entre ${x} e ${y} é ${x}." );
  }  
  else if(x < y){   
    print("O maior número entre ${x} e ${y} é ${y}." );
  }  
  else{
    print("O números são iguais." );
  }
  
}
